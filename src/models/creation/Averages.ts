export class Averages  {
  averageSpeed: string;
  totalDistance: string;
  averagePricePerMile: string;

  constructor(_averageSpeed: string, _totalDistance: string,_averagePricePerMile: string) {
    this.averageSpeed = _averageSpeed;
    this.totalDistance = _totalDistance;
    this.averagePricePerMile = _averagePricePerMile;   
  }
}